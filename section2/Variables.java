public class Variables {
    public static void main(String[] args) {

        // int stype stores 4 bytes
        int passengers = 5;
        System.out.println(passengers);

        int busTickets = passengers;
        System.out.println(busTickets);
    }
}

/**
 * NOTES
 * - int is used for storing whole numbers, no decimals!!
 * - while initialising a variable we need to specify its type.
 * 
 * - int stores 4 bytes, i.e., 32 bits
 * - 32 bits is 4,294,967,296 pieces of data. 
 * - If signed numbers, then the range is from 2,147,483,648 to 2,147,483,647.
 */